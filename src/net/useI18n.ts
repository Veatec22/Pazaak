export function useI18n() {
  const [lang, setLangState] = useState<Language>(currentLang);

  useEffect(() => {
    const handler = (l: Language) => setLangState(l);
    listeners.add(handler);
    return () => {
      listeners.delete(handler);
    };
  }, []);

  const t = useCallback(
    (key: string, params?: Record<string, string | number>) => {
      const dict = (translations[lang] || translations.en) as Record<string, string>;
      let str = dict[key] || (translations.en as Record<string, string>)[key] || key;
      if (params) {
        Object.entries(params).forEach(([k, v]) => {
          str = str.replace(`{${k}}`, String(v));
        });
      }
      return str;
    },
    [lang],
  );

  return { lang, setLanguage, t };
}
